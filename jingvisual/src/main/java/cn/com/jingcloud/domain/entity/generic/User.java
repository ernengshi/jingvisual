package cn.com.jingcloud.domain.entity.generic;

/**
 * generic框架字段 id 必须是普通类型 不能是Long
 *
 * @author liyong
 */
public class User {

    private long id;
    private String name;
    private int age;

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

}
